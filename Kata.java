
public class Kata {
	

	public static boolean even(int number){


	if(number % 2 == 0){

	return true;	

}else{ 

	return false;	

}

}


public static float divide(float firstNumber,float secondNumber){

float numb = firstNumber / secondNumber;

if(secondNumber == 0){

return numb = 0;

} else {

return numb;

}


}



public static int sub(int firstNumber, int secondNumber){


int numb = firstNumber - secondNumber;

return numb;

}




public static int fac(int numb){

int factorial = 1;

for(int i = 1; i <= numb; i++){

	factorial *= i;

}

return factorial;

}






}